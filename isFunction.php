var_dump(is_bool($data));

//tipe data integer  angka tampa titik artinya angka murni(100)
var_dump(is_int($data));

//tipe data floting poin poin lebih dari contoh 100.0 sehingga di sebut float
var_dump(is_float($data));

//tipe data array adalah mengunkan kurung kurawan dau sehingga di sebut array []
var_dump(is_array($data));
