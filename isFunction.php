<?php

$data = "jhelky";

//tipe data string nama mengunkan tanda petik 2 atau 1 "jhelky" 'eto'
var_dump(is_string($data));

//tipe data Null adalah kondisi di mana tidak ada yang ada hanya NULL
var_dump(is_null($data));

